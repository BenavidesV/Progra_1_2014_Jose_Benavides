    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        
        Partidos calendar = new Partidos();
        clsLlenarCalendario cal = new clsLlenarCalendario();
        cal.Llenarcalendario(calendar);
        
        NodoPartidos recP = calendar.getCalendario();
        String clima = "";
        while (recP != null) {
            if (jComboBox1.getSelectedItem() == (recP.getLocal() + "-" + recP.getVisita())) {
                clima = ("Temperatura   " + recP.getTemperatura() + " °C" + "\n"
                        + "Viento   " + recP.getViento() + " m/s" + "\n"
                        + "Humedad   " + recP.getHumedad() + "%");
            }
            txtClima.setText(clima);
            recP= recP.getSiguientePartido();
        }

    }//GEN-LAST:event_jComboBox1ActionPerformed

