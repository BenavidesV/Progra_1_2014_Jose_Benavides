    private void btnPartidosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPartidosActionPerformed
        Partidos calendar = new Partidos();
        clsLlenarCalendario cal = new clsLlenarCalendario();
        cal.Llenarcalendario(calendar);
        
        NodoPartidos recP = calendar.getCalendario();
        String todosPartidos = "";
        while (recP != null) {
            
            if (rbAmazonia.isSelected() && recP.getSede() == "ARENA AMAZONIA") {
                jComboBox1.addItem((recP.getLocal() + "-" + recP.getVisita()));
            }
            if (rbCorinthians.isSelected() && recP.getSede() == "ARENA CORINTHIANS") {
                jComboBox1.addItem((recP.getLocal() + "-" + recP.getVisita()));
            }
            if (rbBaxaida.isSelected() && recP.getSede() == "ARENA DA BAXAIDA") {
                jComboBox1.addItem((recP.getLocal() + "-" + recP.getVisita()));
            }
            if (rbFonteNova.isSelected() && recP.getSede() == "ARENA FONTE NOVA") {
                jComboBox1.addItem((recP.getLocal() + "-" + recP.getVisita()));
            }
            if (rbPantanal.isSelected() && recP.getSede() == "ARENA PANTANAL") {
                jComboBox1.addItem((recP.getLocal() + "-" + recP.getVisita()));
            }
            if (rbPernambuco.isSelected() && recP.getSede() == "ARENA PERNAMBUCO") {
                jComboBox1.addItem((recP.getLocal() + "-" + recP.getVisita()));
            }
            if (rbBeira.isSelected() && recP.getSede() == "ESTADIO BEIRA-RIO") {
                jComboBox1.addItem((recP.getLocal() + "-" + recP.getVisita()));
            }
            if (rbCastelao.isSelected() && recP.getSede() == "ESTADIO CASTELAO") {
                jComboBox1.addItem((recP.getLocal() + "-" + recP.getVisita()));
            }
            if (rbDasDunas.isSelected() && recP.getSede() == "ESTADIO DAS DUNAS") {
                jComboBox1.addItem((recP.getLocal() + "-" + recP.getVisita()));
            }
            if (rbMineirao.isSelected() && recP.getSede() == "ESTADIO MINEIRAO") {
                jComboBox1.addItem((recP.getLocal() + "-" + recP.getVisita()));
            }
            if (rbNacional.isSelected() && recP.getSede() == "ESTADIO NACIONAL") {
                jComboBox1.addItem((recP.getLocal() + "-" + recP.getVisita()));
            }
            if (rbMaracana.isSelected() && recP.getSede() == "ESTADIO MARACANA") {
                jComboBox1.addItem((recP.getLocal() + "-" + recP.getVisita()));
            }
            recP = recP.getSiguientePartido();
        }
        

    }//GEN-LAST:event_btnPartidosActionPerformed

